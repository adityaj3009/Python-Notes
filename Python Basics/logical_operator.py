# logical operators are used to combine conditional statements to form more complex conditions.

# and
x = 5
y = 10

if x > 3 and y > 5:
    print("Both conditions are true")

# -----------------------------------------------------------------

# or 
x = 5
y = 10

if x > 3 or y > 15:
    print("At least one condition is true")

#--------------------------------------------------------------------------------

# not 

x = 5

if not x > 10:
    print("x is not greater than 10")